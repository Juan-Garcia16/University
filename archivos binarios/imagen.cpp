#define YELLOW  "\033[33m"
#define BLACK   "\033[30m"
#define WHITE   "\033[37m"

int main() {
    char image[20][19] = {
        {' ',' ',' ',' ',' ',' ',' ',' ',' ',' ',' ',' ',' ',' ',' ',' ','#',' ',' '},
        {' ',' ',' ',' ',' ','#','#',' ',' ',' ',' ',' ',' ',' ',' ','#','*','#',' '},
        {' ',' ',' ',' ','#','*','#',' ',' ',' ',' ',' ',' ',' ','#','*','*','*','#'},
        {' ',' ',' ',' ','#','*','#',' ',' ',' ',' ','#','#','#','*','*','*','*','#'},
        {' ',' ',' ','#','*','*','#',' ',' ','#','#','#','*','#','*','*','*','#',' '},
        {' ',' ',' ','#','*','*','*','#','#','*','*','#','#','*','*','*','#',' ',' '},
        {' ',' ','#','*','*','*','*','*','*','*','*','#','#','*','*','#','*',' ',' '},
        {' ','#','*','*','*','*','*','*','*','*','*','#','*','#','*','*','#',' ',' '},
        {'#','O','*','*','*','*','*','*','*','*','*','#','*','*','#','*','#',' ',' '},
        {'#','#','*','*','*','*','*','*','*','*','*','*','#','#','*','#',' ',' ',' '},
        {'#','*','*','*','*','O','#','*','*','*','*','*','#','#','#',' ',' ',' ',' '},
        {' ','#','*','*','*','#','#','*','*','*','*','*','*','#',' ',' ',' ',' ',' '},
        {' ',' ','#','*','*','*','*','*','*','*','*','*','*','#',' ',' ',' ',' ',' '},
        {' ','#','*','*','*','*','*','*','*','#','*','*','*','#',' ',' ',' ',' ',' '},
        {' ','#','#','#','*','*','*','*','#','*','*','*','*','#',' ',' ',' ',' ',' '},
        {' ',' ','#','*','#','*','*','*','*','#','*','*','*','#',' ',' ',' ',' ',' '},
        {' ',' ','#','#','#','#','#','*','*','*','*','*','#',' ',' ',' ',' ',' ',' '},
        {' ',' ',' ',' ',' ',' ',' ','#','#','#','*','#','#',' ',' ',' ',' ',' ',' '},
        {' ',' ',' ',' ',' ',' ',' ',' ','#','*','*','*','#',' ',' ',' ',' ',' ',' '},
        {' ',' ',' ',' ',' ',' ',' ',' ',' ','#','#','#',' ',' ',' ',' ',' ',' ',' '}
    };

    // Recorremos la imagen y coloreamos según el carácter
    for (int i = 0; i < 20; i++) {
        for (int j = 0; j < 19; j++) {
            if (image[i][j] == '*') {
                cout << YELLOW << '*' << RESET << " ";  // Asterisco en amarillo
            } else if (image[i][j] == '#') {
                cout << BLACK << '#' << RESET << " ";   // Caracter '#' en negro
            } else if (image[i][j] == 'O') {
                cout << WHITE << 'O' << RESET << " ";   // Caracter 'O' en blanco
            } else {
                cout << image[i][j] << " ";  // Imprimir el carácter original
            }
        }
        cout << endl;
    }

    return 0;
}



